"""
Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:
a) Resultado de suas adições
b) Resultado de suas multiplicações
"""

a = int(input("Informe um número inteiro: "))
b = int(input("Informe o segundo número inteiro: "))
c = int(input("Informe o terceiro número inteiro: "))
d = int(input("Informe o ultimo número inteiro: "))

soma = (a + b + c + d)

print("O valor total dos números é de: ", soma)

vm = (a * b * c * d)

print("O valor de sua multiplicação é de: ", vm)

