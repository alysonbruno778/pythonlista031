"""
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números
"""

a = float(input("Informe um número: "))
b = float(input("Informe outro número: "))
c = float(input("Informe o ultimo número: "))

med =(a + b + c) /3

print("a média aritmética entre estes 3 números é de: ", med)